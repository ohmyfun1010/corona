package kr.co.softcampus.beans;

public class HospitalData {
	private String dates;
	private String possible;
	private String city;
	private String township;
	private String hospitalname;
	private String address;
	private String tel; 
	private String notchangehospitalname;
	public String getNotchangehospitalname() {
		return notchangehospitalname;
	}
	public void setNotchangehospitalname(String notchangehospitalname) {
		this.notchangehospitalname = notchangehospitalname;
	}
	public String getDates() {
		return dates;
	}
	public void setDates(String dates) {
		this.dates = dates;
	}
	public String getPossible() {
		return possible;
	}
	public void setPossible(String possible) {
		this.possible = possible;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTownship() {
		return township;
	}
	public void setTownship(String township) {
		this.township = township;
	}
	public String getHospitalname() {
		return hospitalname;
	}
	public void setHospitalname(String hospitalname) {
		this.hospitalname = hospitalname;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
}
